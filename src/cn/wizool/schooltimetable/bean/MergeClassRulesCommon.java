package cn.wizool.schooltimetable.bean;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonProperty;

public class MergeClassRulesCommon {
	@JsonProperty("taskid") private String taskid;
	@JsonProperty("schoolid") private String schoolid;
	@JsonProperty("mergeclasslist") List<MergeClass> mergeclasslist;
	public String getTaskid() {
		return taskid;
	}
	public void setTaskid(String taskid) {
		this.taskid = taskid;
	}
	public String getSchoolid() {
		return schoolid;
	}
	public void setSchoolid(String schoolid) {
		this.schoolid = schoolid;
	}
	public List<MergeClass> getMergeclasslist() {
		return mergeclasslist;
	}
	public void setMergeclasslist(List<MergeClass> mergeclasslist) {
		this.mergeclasslist = mergeclasslist;
	}
	public MergeClassRulesCommon(String taskid, String schoolid, List<MergeClass> mergeclasslist) {
		super();
		this.taskid = taskid;
		this.schoolid = schoolid;
		this.mergeclasslist = mergeclasslist;
	}
	public MergeClassRulesCommon() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	

}
