package cn.wizool.schooltimetable.bean;

/**
 * 排课时间规则 按班级设置
 * 
 * @author PLJay
 *
 */
public class ClassSettings {

	/**
	 * 记录id
	 */
	public String id = "";
	/**
	 * 学校id
	 */
	public String schoolId = "";
	/**
	 * 任务id
	 */
	public String taskId = "";
	// ------------以下为节次信息
	/**
	 * 单/双周，单1，双2，默认单
	 */
	public int isDoubleWeek = 1;

	/**
	 * 周几，0-13，默认0，不能为其他值,表示单周1至双周日
	 */
	public int weekDay = 0;

	/**
	 * 上课时间段，只能用上午、下午、晚上 貌似此变量在计算时无用
	 */
	public DayPart dayPart = DayPart.AM;

	/**
	 * 当日第几节课
	 */
	public int classNum = 0;

	/**
	 * 班级id
	 * 
	 */
	public String classId;
	/**
	 * 类型 禁止/正常 ：1/0
	 */
	public int type = 1;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(String schoolId) {
		this.schoolId = schoolId;
	}
	public String getTaskId() {
		return taskId;
	}
	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}
	public int getIsDoubleWeek() {
		return isDoubleWeek;
	}
	public void setIsDoubleWeek(int isDoubleWeek) {
		this.isDoubleWeek = isDoubleWeek;
	}
	public int getWeekDay() {
		return weekDay;
	}
	public void setWeekDay(int weekDay) {
		this.weekDay = weekDay;
	}
	public DayPart getDayPart() {
		return dayPart;
	}
	public void setDayPart(DayPart dayPart) {
		this.dayPart = dayPart;
	}
	public int getClassNum() {
		return classNum;
	}
	public void setClassNum(int classNum) {
		this.classNum = classNum;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}

	
}
